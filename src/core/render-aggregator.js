function manifestsGo(aggregator, sources) {
  const entries = sources.map((source) => {
    const json = JSON.stringify(source.manifest, null, 2);
    return `\t{
\t\tSlug:      ${JSON.stringify(source.slug)},
\t\tName:      ${JSON.stringify(source.name)},
\t\tOperation: ${JSON.stringify(source.operation || "")},
\t\tManifest:  ${goRawString(json)},
\t},`;
  }).join("\n");
  return `package aggr

// Sources is the embedded list of source manifests for ${aggregator.slug}.
var Sources = []SourceDescriptor{
${entries}
}
`;
}

