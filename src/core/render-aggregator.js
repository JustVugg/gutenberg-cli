const AGGREGATOR_SCHEMA = "gutenberg.aggregator.v1";

export function loadAggregatorRecipe(recipePath, fs) {
  const content = fs.readFileSync(recipePath, "utf8");
  const recipe = JSON.parse(content);
  if (recipe.schemaVersion && recipe.schemaVersion !== AGGREGATOR_SCHEMA) {
    throw new Error(`Unsupported aggregator recipe schemaVersion: ${recipe.schemaVersion}`);
  }
  if (!Array.isArray(recipe.sources) || recipe.sources.length === 0) {
    throw new Error("aggregator recipe must include sources[]");
  }
  return recipe;
}

export function generateAggregatorProject(recipe, outDir, options = {}) {
  const slug = slugify(options.name || recipe.name || "aggregator");
  const displayName = options.displayName || recipe.displayName || recipe.name || slug;
  const baseDir = options.baseDir || ".";

  const sourceManifests = (recipe.sources || []).map((source) => buildSourceManifest(source, baseDir));

  const aggregatorManifest = {
    schemaVersion: "gutenberg.aggregator-manifest.v1",
    name: displayName,
    slug,
    envPrefix: envPrefix(slug),
    packageName: `gutenberg.local/${slug}`,
    generatedBy: "gutenberg",
    generatedByVersion: "0.1.0",
    language: "go",
    kind: "aggregator",
    description: recipe.description || `Aggregator over ${sourceManifests.length} sources`,
    sources: sourceManifests.map((source) => ({
      slug: source.slug,
      displayName: source.name,
      operation: source.operation,
      baseUrls: source.manifest.baseUrls
    })),
    merge: recipe.merge || "concatenate",
    rank: recipe.rank || "by-source-order"
  };

  assertCanWriteDirectory(outDir, Boolean(options.force));

  writeJson(path.join(outDir, "gutenberg.manifest.json"), aggregatorManifest);
  writeText(path.join(outDir, "go.mod"), goMod(aggregatorManifest));
  writeText(path.join(outDir, "README.md"), readme(aggregatorManifest));
  writeText(path.join(outDir, ".env.example"), envExample(aggregatorManifest, sourceManifests));
  writeText(path.join(outDir, "cmd", aggregatorManifest.slug, "main.go"), aggregatorMain(aggregatorManifest, sourceManifests));
  writeText(path.join(outDir, "internal", "aggr", "manifests.go"), manifestsGo(aggregatorManifest, sourceManifests));
  writeText(path.join(outDir, "internal", "aggr", "client.go"), clientGo(aggregatorManifest));
  writeText(path.join(outDir, "internal", "aggr", "merge.go"), mergeGo(aggregatorManifest));
  writeText(path.join(outDir, "internal", "aggr", "rank.go"), rankGo(aggregatorManifest));
  writeText(path.join(outDir, "internal", "aggr", "types.go"), typesGo(aggregatorManifest));
  writeText(path.join(outDir, "internal", "aggr", "aggr_test.go"), testGo(aggregatorManifest));

  return {
    outDir,
    manifest: aggregatorManifest,
    sourceManifests,
    entrypoint: path.join(outDir, "cmd", aggregatorManifest.slug)
  };
}

function buildSourceManifest(source, baseDir) {
  const specPath = path.resolve(baseDir, source.spec);
  const spec = loadOpenApi(specPath);
  const blueprint = buildBlueprint(spec, specPath, source.name);
  const slug = slugify(source.name || blueprint.slug);
  const operation = source.operation || blueprint.operations[0]?.id || null;
  return {
    slug,
    name: source.name || blueprint.name,
    operation,
    spec: specPath,
    manifest: { ...blueprint, slug }
  };
}

function goMod(manifest) {
  return `module ${manifest.packageName}

go 1.24
`;
}

function envExample(manifest, sources) {
  const lines = [];
  lines.push(`${manifest.envPrefix}_PARALLEL=4`);
  for (const source of sources) {
    const prefix = envPrefix(source.slug);
    lines.push(`${prefix}_API_KEY=`);
    lines.push(`${prefix}_BASE_URL=${source.manifest.baseUrls[0] || ""}`);
  }
  return `${lines.join("\n")}\n`;
}

