import path from "node:path";
import { writeJson, writeText } from "./fs.js";

// OpenClaw-format skill = JSON manifest + human-readable Markdown.
// Compatible surface with several agent frameworks; we keep it neutral.
export function generateOpenClawSkill(manifest, outDir) {
  const skillDir = path.join(outDir, "openclaw", manifest.slug);
  const skillJson = buildSkillJson(manifest);
  writeJson(path.join(skillDir, "skill.json"), skillJson);
  writeText(path.join(skillDir, "skill.md"), buildSkillMarkdown(manifest, skillJson));
  return { skillDir, jsonFile: path.join(skillDir, "skill.json"), mdFile: path.join(skillDir, "skill.md") };
}

function buildSkillJson(manifest) {
  const heroes = Array.isArray(manifest.heroes) ? manifest.heroes : [];
  const operations = Array.isArray(manifest.operations) ? manifest.operations : [];
  const baseUrl = manifest.baseUrls?.[0] || "";

  return {
    schemaVersion: "openclaw.skill.v1",
    name: manifest.slug,
    displayName: manifest.name,
    description: manifest.insights?.thesis || manifest.description || `Generated by Gutenberg from ${manifest.source || "spec"}.`,
    runtime: {
      cli: manifest.slug,
      mcp: manifest.kind === "aggregator" ? null : `${manifest.slug} mcp`
    },
    auth: summarizeAuth(manifest),
    capabilities: {
      readOperations: operations.filter((op) => op.risk === "read").length,
      writeOperations: operations.filter((op) => op.risk === "write").length,
      destructiveOperations: operations.filter((op) => op.risk === "destructive").length,
      cacheable: operations.filter((op) => op.cacheable).length,
      total: operations.length || (Array.isArray(manifest.sources) ? manifest.sources.length : 0)
    },
    actions: heroes.length > 0 ? heroes.slice(0, 30).map((hero) => ({
      name: hero.alias,
      operationId: hero.operationId,
      summary: hero.summary,
      method: hero.method,
      path: hero.path,
      defaultParams: hero.defaultParams || {},
      invoke: `${manifest.slug} ${hero.alias}`,
      safe: true
    })) : (manifest.sources || []).slice(0, 30).map((source) => ({
      name: source.slug,
      operationId: source.operation || source.slug,
      summary: source.displayName || source.slug,
      method: "GET",
      path: source.operation || "",
      defaultParams: {},
      invoke: `${manifest.slug} fan-out`,
      safe: true
    })),
    operations: operations.length > 0 ? operations.slice(0, 200).map((op) => ({
      id: op.id,
      method: op.method,
      path: op.path,
      kind: op.kind || null,
      risk: op.risk,
      summary: op.summary,
      requiredParams: (op.parameters || []).filter((p) => p.required).map((p) => ({ name: p.name, in: p.in, type: p.type })),
      pagination: op.pagination || null,
      invoke: `${manifest.slug} call ${op.id}`
    })) : (manifest.sources || []).map((source) => ({
      id: source.slug,
      method: "GET",
      path: source.operation || "",
      kind: "source",
      risk: "read",
      summary: source.displayName || source.slug,
      requiredParams: [],
      pagination: null,
      invoke: `${manifest.slug} fan-out`
    })),
    baseUrl,
    safety: {
      writeRequiresConfirmation: true,
      confirmationFlag: "--yes",
      dryRunByDefault: true
    },
    provenance: manifest.provenance ? {
      generatedAt: manifest.provenance.generatedAt,
      gutenbergVersion: manifest.provenance.gutenbergVersion,
      specSha: manifest.provenance.spec?.sha256?.slice(0, 12) || null
    } : null
  };
}

function summarizeAuth(manifest) {
  const auth = manifest.auth || {};
  const mode = auth.mode || "none";
  return {
    mode,
    oauth: Boolean(auth.oauth),
    env: mode === "none" ? null : (auth.env || (manifest.envPrefix ? `${manifest.envPrefix}_API_KEY` : null)),
    setupCommand: auth.oauth
      ? `${manifest.slug} auth pkce-start --auth-url <URL> --token-url <URL> --client-id <ID> --redirect-uri <URI>`
      : mode === "none"
        ? null
        : `export ${manifest.envPrefix}_API_KEY=<your-key>`
  };
}

function buildSkillMarkdown(manifest, skill) {
  const lines = [];
  lines.push(`# ${manifest.name} — OpenClaw skill`);
  lines.push("");
  lines.push(`> ${skill.description}`);
  lines.push("");
  lines.push(`- **CLI:** \`${skill.runtime.cli}\``);
  if (skill.runtime.mcp) lines.push(`- **MCP:** \`${skill.runtime.mcp}\``);
  lines.push(`- **Base URL:** ${skill.baseUrl || "(none)"}`);
  lines.push(`- **Operations:** ${skill.capabilities.total} (${skill.capabilities.readOperations} read, ${skill.capabilities.writeOperations} write, ${skill.capabilities.destructiveOperations} destructive)`);
  if (skill.auth.setupCommand) {
    lines.push("");
    lines.push("## Auth setup");
    lines.push("```bash");
    lines.push(skill.auth.setupCommand);
    lines.push("```");
  }
  if (skill.actions.length > 0) {
    lines.push("");
    lines.push("## Actions (zero-friction)");
    for (const action of skill.actions) {
      lines.push(`- \`${action.invoke}\` — ${action.summary}`);
    }
  }
  if (skill.operations.length > 0) {
    lines.push("");
    lines.push("## All operations (first 200)");
    for (const op of skill.operations.slice(0, 50)) {
      lines.push(`- \`${op.invoke}\` (${op.method} ${op.path}) — ${op.kind || op.risk}${op.summary ? ` — ${op.summary}` : ""}`);
    }
    if (skill.operations.length > 50) {
      lines.push(`- … +${skill.operations.length - 50} more — run \`${manifest.slug} operations --json\` for the full list`);
    }
  }
  lines.push("");
  lines.push("## Safety");
  lines.push(`Write/destructive operations dry-run by default. Append \`${skill.safety.confirmationFlag}\` only on explicit user confirmation.`);
  return lines.join("\n");
}
